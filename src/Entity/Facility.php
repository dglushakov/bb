    public function getAddress()
    {
        switch ($this->country) {
            case 'Russia':
                $country = 'Россия';
                break;
            case 'Belarus':
                $country = 'Белоруссия';
                break;
        }


        if (isset($this->streetType)) {
            $strType = self::getStreetTypes()["$this->streetType"];
        } else {
            $strType = '';
        }

        return $country .', '. $this->city . ', ' . $strType . ' ' . $this->street . ', ' . $this->house;
